package com.xindanxin.nutrilife.util;

public class MacroInfo {
    public int calories;
    public int protein;
    public int carbs;
    public int fat;

    public MacroInfo(int calories, int protein, int carbs, int fat) {
        this.calories = calories;
        this.protein = protein;
        this.carbs = carbs;
        this.fat = fat;
    }
}
